// criar metódo para multiplicar 2 números 
static float Multiplicar(float n1, float n2){
    float r = n1 * n2;
    return r;
}

float qlqr = Multiplicar(15, 30);

Console.WriteLine($"Resultado: {qlqr}");

// criar metódo para dividir 2 números 
static float Dividir(float n1, float n2){
    float r = n1 / n2;
    return r;
}

float sla = Dividir(15, 30);

Console.WriteLine($"Resultado: {sla}");

// criar metódo para subtrair 2 números 
static float subtrair(float n1, float n2){
    float r = n1 - n2;
    return r;
}

float kct = subtrair(15, 30);

Console.WriteLine($"Resultado: {kct}");

